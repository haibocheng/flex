package com.adobe.internal.fxg.dom.types;

/**
 * The Text Decoration enumeration.
 * 
 * <pre>
 *   0 = none
 *   1 = underline
 * </pre>
 * 
 * @author Min Plunkett
 */
public enum TextDecoration
{
    /**
     * The enum representing an 'none' Font Style.
     */
    NONE,

    /**
     * The enum representing an 'underline' Font Style.
     */
    UNDERLINE;
}
